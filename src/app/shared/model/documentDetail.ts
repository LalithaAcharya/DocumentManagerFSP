export class DetailsDocument{
    id:any;
    Name:any;
    DocName: any;
    DocType: any;
    DocFile:any;

    constructor(id:any, Name:any, DocName: any,DocType: any, DocFile:any){
        this.id=id;
        this.Name=Name;
        this.DocName=DocName;
        this.DocType=DocType;
        this.DocFile=DocFile;
    }
}
